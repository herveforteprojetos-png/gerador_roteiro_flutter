          ..removeWhere((name) => forbidden.contains(name.toLowerCase()));
        youngMaleAll.shuffle(random);
        final youngMale = youngMaleAll.take(maxNamesPerCategory).toList();
        buffer.writeln('  Jovens (15-35 anos): ${youngMale.join(', ')}');
      }

      if (langData['masculino']!.containsKey('maduro')) {
        // 🎲 EMBARALHAR lista antes de pegar os nomes
        final matureMaleAll = List<String>.from(
          langData['masculino']!['maduro']!,
        )
          // 🆕 v7.6.29: FILTRAR nomes proibidos ANTES de embaralhar
          ..removeWhere((name) => forbidden.contains(name.toLowerCase()));
        matureMaleAll.shuffle(random);
        final matureMale = matureMaleAll.take(maxNamesPerCategory).toList();
        buffer.writeln('  Maduros (36-60 anos): ${matureMale.join(', ')}');
      }

