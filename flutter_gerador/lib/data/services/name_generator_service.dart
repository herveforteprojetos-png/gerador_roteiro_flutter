          langData['masculino']!['idoso']!,
        )
          // 🆕 v7.6.29: FILTRAR nomes proibidos ANTES de embaralhar
          ..removeWhere((name) => forbidden.contains(name.toLowerCase()));
        elderlyMaleAll.shuffle(random);
        final elderlyMale = elderlyMaleAll.take(maxNamesPerCategory).toList();
        buffer.writeln('  Idosos (60+ anos): ${elderlyMale.join(', ')}');
      }

      buffer.writeln('');
    }

    // Feminino
    if (langData.containsKey('feminino')) {
      buffer.writeln('FEMININO:');

      if (langData['feminino']!.containsKey('jovem')) {
