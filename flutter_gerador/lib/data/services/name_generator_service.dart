  /// Mapeia nome do idioma para código ISO 639-1
  static String _getLanguageCode(String language) {
    final normalized = language.toLowerCase().trim();

    // Mapeamento de nomes de idiomas para códigos ISO
    if (normalized.contains('português') ||
        normalized.contains('portugues') ||
        normalized == 'pt')
      return 'pt';
    if (normalized.contains('inglês') ||
        normalized.contains('ingles') ||
        normalized.contains('english') ||
        normalized == 'en')
      return 'en';
    if (normalized.contains('espanhol') ||
        normalized.contains('spanish') ||
        normalized.contains('español') ||
        normalized == 'es')
      return 'es';
    if (normalized.contains('francês') ||
        normalized.contains('frances') ||
        normalized.contains('french') ||
        normalized.contains('français') ||
        normalized == 'fr')
      return 'fr';
    if (normalized.contains('alemão') ||
        normalized.contains('alemao') ||
        normalized.contains('german') ||
        normalized.contains('deutsch') ||
        normalized == 'de')
      return 'de';
    if (normalized.contains('italiano') ||
        normalized.contains('italian') ||
        normalized == 'it')
      return 'it';
    if (normalized.contains('russo') ||
        normalized.contains('russian') ||
        normalized == 'ru')
      return 'ru';
    if (normalized.contains('turco') ||
        normalized.contains('turkish') ||
        normalized.contains('türk') ||
        normalized == 'tr')
      return 'tr';
    if (normalized.contains('croata') ||
        normalized.contains('croatian') ||
        normalized.contains('hrvatski') ||
        normalized == 'hr')
      return 'hr';
    if (normalized.contains('polonês') ||
        normalized.contains('polones') ||
        normalized.contains('polish') ||
        normalized.contains('polski') ||
        normalized == 'pl')
      return 'pl';
    if (normalized.contains('búlgaro') ||
        normalized.contains('bulgaro') ||
        normalized.contains('bulgarian') ||
        normalized == 'bg')
      return 'bg';
    if (normalized.contains('romeno') ||
        normalized.contains('romanian') ||
        normalized.contains('română') ||
        normalized == 'ro')
      return 'ro';
    if (normalized.contains('coreano') ||
        normalized.contains('korean') ||
        normalized.contains('한국어') ||
        normalized == 'ko')
      return 'ko';

    // Fallback para português
    return 'pt';
  }

  static String getNameListForPrompt({
    required String language,
    String? genre,
    int maxNamesPerCategory = 30, // Limitar para não sobrecarregar prompt
    Set<String>? excludedNames, // 🆕 v7.6.29: Nomes proibidos (já usados)
  }) {
    final buffer = StringBuffer();

    // 🆕 v7.6.29: Criar conjunto de nomes proibidos (case-insensitive)
    final forbidden = (excludedNames ?? {}).map((n) => n.toLowerCase()).toSet();

    if (forbidden.isNotEmpty) {
      print('🚫 v7.6.29: Filtrando ${forbidden.length} nomes já usados da lista disponível');
      print('   Nomes proibidos: ${forbidden.join(", ")}');
    }

    // 🎲 CRIAR GERADOR DE NÚMEROS ALEATÓRIOS (seed baseado em timestamp)
    // Garante que cada geração terá ordem diferente de nomes
    final random = Random(DateTime.now().millisecondsSinceEpoch);

    // Se for western, usar apenas nomes temáticos
    if (genre == 'western') {
      buffer.writeln(
        'NOMES WESTERN (use APENAS estes para histórias de faroeste):',
      );

      if (_westernNames.containsKey('masculino')) {
        // 🎲 EMBARALHAR lista antes de pegar os nomes
        final maleNamesAll = List<String>.from(
          _westernNames['masculino']!['todos']!,
        )
          // 🆕 v7.6.29: FILTRAR nomes proibidos ANTES de embaralhar
          ..removeWhere((name) => forbidden.contains(name.toLowerCase()));
        maleNamesAll.shuffle(random);
        final maleNames = maleNamesAll.take(maxNamesPerCategory).toList();
        buffer.writeln('  Masculinos: ${maleNames.join(', ')}');
      }

      if (_westernNames.containsKey('feminino')) {
        // 🎲 EMBARALHAR lista antes de pegar os nomes
        final femaleNamesAll = List<String>.from(
          _westernNames['feminino']!['todos']!,
        )
          // 🆕 v7.6.29: FILTRAR nomes proibidos ANTES de embaralhar
          ..removeWhere((name) => forbidden.contains(name.toLowerCase()));
        femaleNamesAll.shuffle(random);
        final femaleNames = femaleNamesAll.take(maxNamesPerCategory).toList();
        buffer.writeln('  Femininos: ${femaleNames.join(', ')}');
      }

      return buffer.toString();
    }

    // 🌍 MAPEAR NOME DO IDIOMA PARA CÓDIGO ISO
    var languageCode = _getLanguageCode(language);

    // Nomes normais por idioma
    if (!_namesDatabase.containsKey(languageCode)) {
      languageCode = 'pt'; // Fallback para português
    }

    final langData = _namesDatabase[languageCode]!;

    buffer.writeln('NOMES DISPONÍVEIS (use APENAS nomes desta lista):');
    buffer.writeln('');

    // Masculino
    if (langData.containsKey('masculino')) {
